    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        JTextField[] txtFields = {
            txtAutonomia, txtTempoViagem, txtValorComb, txtVelocidade
        };
        if (!verificarVazio())return;
        if (!verificarValores(txtFields))return;
        
        
        viagem2 viagem = new viagem2();
        
        viagem.setAutonomia(Double.parseDouble(txtAutonomia.getText()));
        viagem.setValorComb(Double.parseDouble(txtValorComb.getText()));
        viagem.setTempoViagem(Integer.parseInt(txtTempoViagem.getText()));
        viagem.setVelocidade(Double.parseDouble(txtVelocidade.getText()));
        
        String saida = viagem.calcular();
        
        JOptionPane.showMessageDialog(null, viagem.calcular());
 
        txtAutonomia.requestFocus();
    }//GEN-LAST:event_btnCalcularActionPerformed

