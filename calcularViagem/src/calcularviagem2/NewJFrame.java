    private boolean verificarVazio(){
        if (txtAutonomia.getText(). trim(). length() == 0 ){
            JOptionPane.showMessageDialog(null,"Forneça a autonomia");
            txtAutonomia.requestFocus();
            return false;
        }
        if (txtTempoViagem.getText(). trim(). length() == 0 ){
            JOptionPane.showMessageDialog(null,"Forneça o tempo de viagem");
            txtTempoViagem.requestFocus();
            return false;
        }
        if (txtValorComb.getText(). trim(). length() == 0 ){
            JOptionPane.showMessageDialog(null,"Forneça o valor do combustivel");
            txtValorComb.requestFocus();
            return false;
        }
        if (txtVelocidade.getText(). trim(). length() == 0 ){
            JOptionPane.showMessageDialog(null,"Forneça a velocidade");
            txtVelocidade.requestFocus();
            return false;
        }
        return true;
    }
    private boolean verificarValores(JTextField[] txtFields){
        int errorCounter = 0;
        for (int i=0; i < txtFields.length; i++){
            try {
                Double.parseDouble(txtFields[i].getText());
            }catch (NumberFormatException e){
                JOptionPane.showMessageDialog(null, " O valor " + txtFields[i].getText() + "Não é um numero!");
                txtFields[i].setText("");
                errorCounter++;
        }    
    }
        return errorCounter == 0;
  }     
    
